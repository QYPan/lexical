//
// 指针 - 指针
// 条件：两个指针都指向同一个数组中的元素
// 指针相减结果的类型为 ptrdiff_t（有符号整型），代表两个指针在内存中的距离
// （以数组元素为长度单位，而不是以字节为单位）
//
